    class Server
    {
        public static int MaxPlayers { get; private set; }
        public static int Port { get; private set; }

        private static TcpListener tcpListener;

        private static Dictionary<int, Client> clients;

        public static void Start(int maxPlayers, int port)
        {
            MaxPlayers = maxPlayers;
            Port = port;

            Console.WriteLine("Starting server...");
            InitializeServerData();

            tcpListener = new TcpListener(IPAddress.Any, port);
            tcpListener.Start();
            tcpListener.BeginAcceptTcpClient(TCPConnectCallBack, null);

            Console.WriteLine($"Server started on port {Port}.");
        }

        private static void TCPConnectCallBack(IAsyncResult result)
        {
            TcpClient tcpClient = tcpListener.EndAcceptTcpClient(result);

            tcpListener.BeginAcceptTcpClient(TCPConnectCallBack, null);

            Console.WriteLine($"Incoming connection from {tcpClient.Client.RemoteEndPoint}...");

            for (int i = 1; i <= MaxPlayers; i++)
            {
                var _client = clients[i];
                if (_client.tcp.socket == null)
                {
                    _client.tcp.Conntct(tcpClient);
                    return;
                }
            }
            Console.WriteLine($"{tcpClient.Client.RemoteEndPoint} failed to connect: Server full!");
        }

        private static void InitializeServerData()
        {
            clients = new Dictionary<int, Client>(MaxPlayers);

            for (int i = 1; i <= MaxPlayers; i++)
            {
                clients.Add(i, new Client(i));
            }
        }
    }

    class Client
    {
        private static int dataBufferSize = 2048;

        public int Id;
        public Tcp tcp;

        public Client(int id)
        {
            Id = id;
            tcp = new Tcp(id);
        }

        public class Tcp
        {
            public TcpClient socket;

            private readonly int id;
            private NetworkStream stream;
            private byte[] receiveBuffer;

            public Tcp(int id)
            {
                this.id = id;
            }
            public void Conntct(TcpClient socket)
            {
                this.socket = socket;
                this.socket.ReceiveBufferSize = dataBufferSize;
                this.socket.SendBufferSize = dataBufferSize;
                receiveBuffer = new byte[dataBufferSize];

                stream = this.socket.GetStream();


                stream.BeginRead(receiveBuffer, 0, dataBufferSize, ReceiveCallBack, null);
            }

            private void ReceiveCallBack(IAsyncResult result)
            {
                try
                {
                    int byteLength = stream.EndRead(result);
                    if (byteLength <= 0)
                    {
                        return;
                    }

                    byte[] data = new byte[byteLength];
                    Array.Copy(receiveBuffer, data, byteLength);

                    string msg = Encoding.ASCII.GetString(receiveBuffer, 0, byteLength);
                    Console.WriteLine(msg);
                    Console.WriteLine("Cur Time " + Program.GetTimeStamps());

                    stream.BeginRead(receiveBuffer, 0, dataBufferSize, ReceiveCallBack, null);
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"Error receiving TCP data: {ex}");
                }

            }
        }
    }

